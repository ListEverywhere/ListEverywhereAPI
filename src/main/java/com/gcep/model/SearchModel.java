package com.gcep.model;

public class SearchModel {
	
	private String search;
	
	public SearchModel() {
		this.search = null;
	}

	public SearchModel(String search) {
		super();
		this.search = search;
	}

	public String getSearch() {
		return search;
	}

	public void setSearch(String search) {
		this.search = search;
	}
	
	

}
